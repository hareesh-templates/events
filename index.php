                <p>I took an initiative in summer 2021 to educate people in rural South India on the COVID vaccine and give them the opportunity to get vaccinated. I came up with this idea when I was planning a trip to India and realized how destructive the Delta variant was there, which really affected me because of the sense of home I have always found in India and my large extended family who lives there. I wanted to see if there were any opportunities for change in villages where there were no resources for COVID protection. </p>
                <p>At each village, we organized talks by doctors, village leaders, and myself about the importance and safety of getting vaccinated, followed by makeshift vaccine clinics in schoolhouses and similar locations. Our project earned support from local politicians and promotion by the local press. After visiting 5 villages with an average adult population of 500, we administered 500 first doses of CoviShield to around 100 people at each village, and gave each vaccine recipient an incentive bag of useful items.</p>
