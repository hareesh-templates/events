                          <h4 style="text-align:center;">  E A Baking Donation Project</h4>
<p>During my sophomore year, I was alternating between periods of in-person and virtual learning amidst several waves of the COVID pandemic. I found myself spending a lot of time baking while I quarantined indoors. I wanted to bake regularly but didn't want the baked goods to go to waste, so I decided to reach out to CHOC in Norristown, PA, which is a homeless shelter serving the greater area. I started baking for their residents every week from August of 2020 until around June 2021, when I couldn't stick as strictly to a weekly schedule because of summer activities. Every Sunday, I baked 40 cupcakes, switching varieties every week so the residents could try a different type of cupcake each time. At first, my family was sponsoring the ingredients needed for the cupcakes, which came to around 10 dollars each week, but  friends and family members of ours began to ask if they could sponsor the donations. Every week 1 or2 people would sponsor the ingredients and I would donate the cupcakes to CHOC under their name or with a message of their choice. It was a really great way to give back to my community and the people who were being hit hard the most during the pandemic while also improving my baking skills and occupying myself while I was spending so much time at home. Here are a few of the cupcakes that I donated to CHOC:</p><br>
